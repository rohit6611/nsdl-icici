package com.altruist.response;

import java.util.List;

import com.altruist.nsdl.model.CustomerDetailModel;

public class CustomerDetailListResponse {

	private StatusDescription statusDescription;
	private List<CustomerDetailModel> customerList;

	public StatusDescription getStatusDescription() {
		return statusDescription;
	}

	public void setStatusDescription(StatusDescription statusDescription) {
		this.statusDescription = statusDescription;
	}

	public List<CustomerDetailModel> getCustomerList() {
		return customerList;
	}

	public void setCustomerList(List<CustomerDetailModel> customerList) {
		this.customerList = customerList;
	}

	@Override
	public String toString() {
		return "CustomerDetailListResponse [statusDescription=" + statusDescription + ", customerList=" + customerList
				+ "]";
	}

}
