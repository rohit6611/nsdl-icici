package com.altruist.response;

public class QuotationResponse {

	private StatusDescription statusDescription;
	private String basicPremium;
	private String totalTax;
	private String totalPremium;

	public StatusDescription getStatusDescription() {
		return statusDescription;
	}

	public void setStatusDescription(StatusDescription statusDescription) {
		this.statusDescription = statusDescription;
	}

	public String getBasicPremium() {
		return basicPremium;
	}

	public void setBasicPremium(String basicPremium) {
		this.basicPremium = basicPremium;
	}

	public String getTotalTax() {
		return totalTax;
	}

	public void setTotalTax(String totalTax) {
		this.totalTax = totalTax;
	}

	public String getTotalPremium() {
		return totalPremium;
	}

	public void setTotalPremium(String totalPremium) {
		this.totalPremium = totalPremium;
	}

	@Override
	public String toString() {
		return "QuotationResponse [statusDescription=" + statusDescription + ", basicPremium=" + basicPremium
				+ ", totalTax=" + totalTax + ", totalPremium=" + totalPremium + "]";
	}

}
