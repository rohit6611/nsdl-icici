package com.altruist.response;

import java.util.List;

import com.altruist.nsdl.model.LocalityModel;

public class PinCodeResponse {

	private StatusDescription statusDescription;
	private List<String> localityList;

	public StatusDescription getStatusDescription() {
		return statusDescription;
	}

	public List<String> getLocalityList() {
		return localityList;
	}

	public void setStatusDescription(StatusDescription statusDescription) {
		this.statusDescription = statusDescription;
	}

	public void setLocalityList(List<String> localityList) {
		this.localityList = localityList;
	}

	@Override
	public String toString() {
		return "PinCodeResponse [statusDescription=" + statusDescription + ", localityList=" + localityList + "]";
	}

}
